package com.miraiprjkt.foundation.repeater;

public class For {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println("Mirai Cantik banget");
        }

        for (int i = 1; i <= 10; i++) {
            System.out.println("Angka ke-" + i);
        }
    }
}
